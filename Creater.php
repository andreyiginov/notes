<?php

/**
 * Запрашивает у пользователя текст для заметки и возвращает его.
 *
 * @return string Текст заметки, введенный пользователем.
 */
function getNoteFromUser() {
    // Выводим инструкцию для пользователя
    echo "Введите текст вашей заметки (для завершения введите 'END' на новой строке):\n";
    
    // Инициализируем пустую строку для хранения текста заметки
    $note = "";
    
    // Начинаем бесконечный цикл для ввода строк
    while (true) {
        // Считываем строку ввода и удаляем лишние пробелы в начале и конце
        $line = trim(fgets(STDIN));
        
        // Проверяем, не является ли введенная строка сигналом к завершению ввода
        if ($line === "END") {
            // Если да, прерываем цикл
            break;
        }
        // Добавляем введенную строку к тексту заметки с переносом строки
        $note .= $line . "\n";
    }
    
    // Возвращаем полный текст заметки
    return $note;
}

